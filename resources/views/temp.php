SELECT BROW.NUM_BROW, brom.COD_DPT, EEMPB.NAM_EMP, BROM.EMP_BROW, BROW.EMP_LST, BROM.COD_CUST, BROW.COD_ITEM, BROM.dat_brow, BROW.dat_rrtn, BROW.dat_artn, BROW.sts_brow, BROW.cnt_artn, brom.cls_brow, FACT.NAM_FACTS
FROM BROW
LEFT JOIN BROM ON BROW.NUM_BROW = BROM.NUM_BROW
LEFT JOIN FACT ON FACT.COD_FACT = BROM.COD_CUST
LEFT JOIN EEMPB ON EEMPB.COD_EMP = BROM.EMP_BROW
WHERE 1 = 1
  AND (BROM.NUM_BROW LIKE 'LD%')
  AND (BROW.sts_brow <> 50 AND BROW.sts_brow <> 80)
  AND (BROW.EMP_LST NOT LIKE 'I%')
